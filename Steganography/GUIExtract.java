    private void proccessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proccessButtonActionPerformed
        boolean isStegoMedia = !stegoImageTextField.getText().isEmpty();
        boolean isKeyMessage = !keyTextField.getText().isEmpty();
        boolean isBMP = stegoImageTextField.getText().contains(".bmp");
        System.out.println("isStegoMedia = "+isStegoMedia);
        System.out.println("isKeyMessage = "+ isKeyMessage);
        System.out.println("isBMP = "+ isBMP);
        
        if (isStegoMedia && isKeyMessage && isBMP) {
            CoverMedia stegoImage = null;
            String key = "";
            String res = "";
            try {
                stegoImage = new CoverMedia(stegoImageTextField.getText());
                try {
                    stegoImage.ImageType();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(rootPane, "Format Gambar harus bitmap!");
                }
            } catch (IOException ex) {
                Logger.getLogger(GUIExtract.class.getName()).log(Level.SEVERE, null, ex);
            }
            {
                key = keyTextField.getText();
                RppRPE rpp = new RppRPE();
                if (rpp.keyValidation(key)) {
                    res = rpp.extractMessage(key, stegoImage);
                    secretMessageTextField.setText(res);
                } else {
                    keyNotValid();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Periksa kembali inputan dan metode yang dipilih");
        }
    }//GEN-LAST:event_proccessButtonActionPerformed

