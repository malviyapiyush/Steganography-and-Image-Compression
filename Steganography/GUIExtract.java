    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        file.setDialogTitle("Choose image : ");
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "bmp");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            try {
                image = ImageIO.read(selectedFile);
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(GUIHide.class.getName()).log(Level.SEVERE, null, ex);
            }
            String path = selectedFile.getAbsolutePath();
            stegoImageTextField.setText(path);
        } else if (result == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(rootPane, "Periksa kembali inputan dan metode yang dipilih");
        }
    }//GEN-LAST:event_browseButtonActionPerformed

